package com.example.domain;

public class CarSpotVO {
	private String eltc_yn;
	private String str_pnt_id;
	private double lot;
	private String str_pnt_name;
	private String addr;
	private double lat;

	public String getEltc_yn() {
		return eltc_yn;
	}

	public void setEltc_yn(String eltc_yn) {
		this.eltc_yn = eltc_yn;
	}

	public String getStr_pnt_id() {
		return str_pnt_id;
	}

	public void setStr_pnt_id(String str_pnt_id) {
		this.str_pnt_id = str_pnt_id;
	}

	public double getLot() {
		return lot;
	}

	public void setLot(double lot) {
		this.lot = lot;
	}

	public String getStr_pnt_name() {
		return str_pnt_name;
	}

	public void setStr_pnt_name(String str_pnt_name) {
		this.str_pnt_name = str_pnt_name;
	}

	public String getAddr() {
		return addr;
	}

	public void setAddr(String addr) {
		this.addr = addr;
	}

	public double getLat() {
		return lat;
	}

	public void setLat(double lat) {
		this.lat = lat;
	}

}
